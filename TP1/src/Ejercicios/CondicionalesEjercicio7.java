public class CondicionalesEjercicio7 {

    public void esPar(int numero)
    {

        if(numero % 2 ==0)
        {
            System.out.println("El numero " + numero + " es par");
        }else
        {
            System.out.println("El numero " + numero + " es impar");
        }
    }

}
